package Calculus;

public class Calculus {
    public static void Add(int num1, int num2) {
        System.out.print("The Addition result is: ");
        System.out.println(num1 + num2);
    }
    public static void Subt(int num1,int num2) {
        System.out.print("The Subtraction result is: ");
        System.out.println(num1 - num2);
    }
    public static void Mult(int num1, int num2) {
        System.out.print("The Multiplaction result is: ");
        System.out.println(num1 * num2);
    }
    public static void Divs(int num1, int num2) {
        System.out.print("The Division result is: ");
        System.out.println(num1/num2);
    }
}